<template>
  <div id="single-blog">
    <h1>{{ blog.title }}</h1>
    <article>{{ blog.content }}</article>
  </div>
</template>
<script>
export default {
  data() {
    return {
      id: this.$route.params.id,
      blog: {},
    };
  },
  created() {
    this.$http
      .get(`https://vue-ninja-40e81.firebaseio.com/posts/${this.id}.json`)
      .then((data) => (this.blog = data.body));
  },
};
</script>
<style scoped>
#single-blog {
  max-width: 960px;
  margin: 0 auto;
}
</style>